// Write a Program to calculate and display the volume of a CUBE having its height (h=10cm), width 
// (w=12cm) and depth (8cm).
#include <stdio.h>
void main(){
    int h=10,w=12,d,volume;
    printf("volume of a CUBE having its height = 10cm, width = 12cm, depth = 8cm\n");
    volume = h*w*d;
    printf("Volume of the CUBE: %d cm3", volume);
}